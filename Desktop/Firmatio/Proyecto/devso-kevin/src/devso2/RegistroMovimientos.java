    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int n = JOptionPane.showConfirmDialog(this,
            "¿Desea eliminar el registro "+ tableEgresos.getValueAt(tableEgresos.getSelectedRow(), 0).toString() +"?","Confirmación",
            JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION){

            con=new Conexion();
            con.Conectar();
            try{
                con.DeleteMov(Integer.parseInt( tableEgresos.getValueAt(tableEgresos.getSelectedRow(), 0).toString()));
                expenses=con.GetMovesCajas();
            } catch (SQLException ex) {
            }
            con.Desconectar();
            JOptionPane.showMessageDialog(this,"Movimiento de caja eliminado con éxito.");

            int j=model.getRowCount();
            for(int i=0;i<j;i++){
                model.removeRow(j-i-1);
            }
              for(Moves_Account obj: expenses)
        {
            model.addRow(new Object[]{obj.Id,obj.Date,obj.AccountOut,obj.AccountIn,obj.Description,obj.Amount});
        }
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

